#include <iostream>
using namespace std;

int main()
{
    const int MAX_SIZE = 100; // กำหนดขนาดสูงสุดของข้อมูล
    int data[MAX_SIZE];
    int n;

    // รับจำนวนข้อมูล
    cout << "ป้อนจำนวนข้อมูล: ";
    cin >> n;

    if (n <= 0 || n > MAX_SIZE)
    {
        cout << "จำนวนข้อมูลไม่ถูกต้อง" << endl;
        return 0;
    }

    // รับค่าข้อมูลจากผู้ใช้
    cout << "ป้อนตัวเลข " << n << " ค่า: ";
    for (int i = 0; i < n; i++)
    {
        cin >> data[i];
    }

    // คำนวณช่วงเปอร์เซ็นไทล์ตามลำดับที่ป้อน
    int sum_0_50 = 0, sum_51_80 = 0, sum_81_100 = 0;

    for (int i = 0; i < n; i++)
    {
        if (i < n * 0.50)
        { // 0-50%
            sum_0_50 += data[i];
        }
        else if (i < n * 0.80)
        { // 51-80%
            sum_51_80 += data[i];
        }
        else
        { // 81-100%
            sum_81_100 += data[i];
        }
    }

    // แสดงผลลัพธ์
    cout << "ผลรวมของช่วง 0-50%: " << sum_0_50 << endl;
    cout << "ผลรวมของช่วง 51-80%: " << sum_51_80 << endl;
    cout << "ผลรวมของช่วง 81-100%: " << sum_81_100 << endl;

    return 0;
}

//ป้อนจำนวนข้อมูล: 10
//ป้อนตัวเลข 10 ค่า: 5 10 15 20 25 30 35 40 45 50

// ผลรวมของช่วง 0-50%: 75
// ผลรวมของช่วง 51-80%: 105
// ผลรวมของช่วง 81-100%: 95
